        <section class="mb-6">
            <h2 class="text-xl font-semibold mb-2">§1 Name, Sitz des Vereins, Rechtsform und Geschäftsjahr</h2>
            <p>Der Verein führt den Namen „Offizieller MADDRAX-Fanclub“ und hat seinen Sitz in Potsdam, Brandenburg. Das Geschäftsjahr ist das Kalenderjahr.</p>
        </section>
        <section class="mb-6">
            <h2 class="text-xl font-semibold mb-2">§2 Zweck und Ziele</h2>
            <p>Sinn und Zweck des Vereins sind:</p>
            <ul class="list-disc ml-6">
                <li>die Förderung des Austauschs von Fans der Romanserie MADDRAX – Die dunkle Zukunft der Erde,</li>
                <li>die Förderung von Kunst und Kultur im phantastischen Bereich,</li>
                <li>die Förderung der Volksbildung im Bereich der phantastischen Literatur sowie</li>
                <li>die Förderung der Völkerverständigung.</li>
            </ul>
