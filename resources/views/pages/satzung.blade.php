        <section class="mb-6">
            <h2 class="text-xl font-semibold mb-2">§3 Mitgliedschaft</h2>
            <h3 class="font-semibold">Ordentliche Mitgliedschaft</h3>
            <p>Jede natürliche und juristische Person kann ordentliches Mitglied werden. Der Aufnahmeantrag ist gleichzeitig die Anerkennung der Satzung und des Verhaltenskodex (Code of Conduct). Juristische Personen erwerben durch ihren Beitritt eine Mitgliedschaft mit nur einer Stimme (in Ziffer: 1). Der Antrag kann elektronisch übermittelt werden. Jedes ordentliche Mitglied hat die Pflicht den Mitgliedsbeitrag zu entrichten.</p>

            <h3 class="font-semibold mt-4">Ehrenmitgliedschaft</h3>
            <p>Allen regelmäßigen Autor:innen und Coverkünstler:innen wird mit der dritten Veröffentlichung für die MADDRAX-Serie die Ehrenmitgliedschaft angeboten. Ehrenmitglieder erhalten ebenfalls alle Befugnisse und Zugänge, die ein ordentliches Mitglied auch erhält.</p>

            <h3 class="font-semibold mt-4">Rechte und Pflichten der Mitglieder</h3>
            <p>Jedes Mitglied hat das Recht an den Veranstaltungen des Vereins teilzunehmen. Mit Beginn der Mitgliedschaft verpflichtet sich das Mitglied den Verhaltenskodex (Code of Conduct) des Vereins einzuhalten. Satzung und Verhaltenskodex in den jeweilig geltenden Fassungen müssen vor der Stellung eines Antrags auf Aufnahme in den Verein eingesehen werden können.</p>
        </section>
        <section class="mb-6">
            <h2 class="text-xl font-semibold mb-2">§4 Mitgliedsbeiträge</h2>
            <p>Der Mitgliedsbeitrag ist jährlich vorab zu begleichen. Die Höhe des Mitgliedsbeitrags darf frei gewählt werden, aber soll den Mindestbetrag von 1,00 Euro pro Monat nicht unterschreiten. Die Mitgliedschaft wird erst mit Geldeingang wirksam und gilt für die nachfolgenden Monate. Ehrenmitgliedern wird der Mitgliedsbeitrag erlassen. Spenden sind jederzeit an das Vereinskonto möglich. Spenden in Form von Fanartikeln oder Büchern sind ebenfalls möglich, sollen jedoch vorher mit dem Vorstand abgesprochen werden.</p>
        </section>
        <section class="mb-6">
            <h2 class="text-xl font-semibold mb-2">§5 Austritt</h2>
            <p>Die Mitgliedschaft endet mit Austritt, Ausschluss oder Tod; bei juristischen Personen entspricht das deren Erlöschen. Eine formlose E-Mail an den Vorstand reicht aus, um die Mitgliedschaft zu kündigen. Eine Rückzahlung von bereits geleisteten Mitgliedsbeiträgen ist nicht vorgesehen. Über einen Ausschluss entscheidet der Vorstand durch eine qualifizierte Mehrheit (2/3 Mehrheit).</p>
        </section>
        <section class="mb-6">
            <h2 class="text-xl font-semibold mb-2">§6 Organe</h2>
            <h3 class="font-semibold">Mitgliederversammlung</h3>
            <p>Eine Mitgliederversammlung findet mindestens jährlich statt. Zu ihr lädt der Vorstand mindestens vier Wochen im Voraus alle Mitglieder schriftlich ein. Die Einladung erfolgt vorrangig elektronisch. Zur jährlichen Mitgliederversammlung treffen sich die Mitglieder persönlich; die Versammlung kann jedoch auch online durchgeführt werden. Die Entscheidung hierzu trifft der Vorstand. Die Mitgliederversammlung wird durch den oder die 1. Vorsitzende:n oder einem von ihm oder ihr bestimmten Vertreter geleitet.</p>
            <p class="mt-2">Über den Ablauf der Mitgliederversammlung und die gefassten Beschlüsse ist ein Protokoll zu fertigen, das vom Protokollführer und vom Versammlungsleiter zu unterschreiben ist.</p>

            <h3 class="font-semibold mt-4">Newsletter</h3>
            <p>Der Vorstand ist bestrebt mindestens halbjährlich in einem Newsletter über den Stand der Vereinsprojekte sowie anstehender Termine zu informieren.</p>
        </section>
        <section class="mb-6">
            <h2 class="text-xl font-semibold mb-2">§7 Vorstand</h2>
            <p>Der Vorstand wird für vier Jahre gewählt. Alle Vorstandsmitglieder müssen auch gleichzeitig Mitglied im Sinne der Satzung sein. Alle Mitglieder sind stimmberechtigt. Mitglieder können zur Wahl vorgeschlagen werden oder sich selbst für ein Amt zur Verfügung stellen. Im Falle einer Nominierung muss das Mitglied diese vor der Wahl annehmen, um für die Wahl aufgestellt zu werden.</p>
            <p>Zu besetzende Posten:
                <ul class="list-disc ml-6">
                    <li>1. Vorsitzende:r</li>
                    <li>2. Vorsitzende:r</li>
                    <li>Kassenwart:in</li>
                </ul>
            </p>
            <p>Dem Vorstand des Vereins obliegen die Vertretung des Vereins nach § 26 BGB und die Führung seiner Geschäfte.
                Bei Rechtsgeschäften, die der Vorstand im Namen des Vereins vornimmt, haften die Mitglieder nur mit dem Vereinsvermögen. Scheidet ein Mitglied vorzeitig aus dem Vorstand aus, so sind die verbleibenden Mitglieder des Vorstands berechtigt, ein Mitglied des Vereins bis zur Wahl des Nachfolgers durch die Mitgliederversammlung in den Vorstand zu wählen. Die Mitgliederversammlung kann ein Vorstandsmitglied mit der Ausübung zweier Vorstandsämter betrauen.</p>
        </section>
        <section>
            <h2 class="text-xl font-semibold mb-2">§8 Mittel</h2>
            <p>Die Mittel des Vereins sind bestimmt für
                <ul class="list-disc ml-6">
                    <li>die Deckung der Serverkosten des Maddraxikons sowie des MADDRAX-Fanblogs,</li>
                    <li>zur Finanzierung eines jährlichen Fantreffens</li>
                    <li>sowie zur finanziellen und materiellen Unterstützung anderer Fanprojekte innerhalb des MADDRAX-Fandoms.</li>
            </p>
            <p>Sollte sich der Verein durch Mehrheitsbeschluss auflösen, werden sämtliche Mittel aus der Vereinskasse dazu genutzt, das Hosting für das Wiki des Maddraxikons so lange wie möglich zu bezahlen.</p>
        </section>
        <section class="mb-6">
            <h2 class="text-xl font-semibold mb-2">§9 Kassenprüfung</h2>
            <p>Die Mitgliederversammlung wählt für die Dauer von einem Jahr eine:n Kassenprüfer:in. Diese:r darf nicht Mitglied des Vorstands sein. Wiederwahl ist zulässig.</p>
